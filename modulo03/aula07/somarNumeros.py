#Função para somar dois números

def soma(numero1, numero2):
    return (numero1 + numero2)

calc1 = soma(10, 5)
print(calc1)

calc2 = soma(3, 3)
print(calc2)

calc3 = soma(10, 2)
print(calc3)

resultado = soma(1,2)
print(resultado)

resultado = soma(2, 2) * 3
print(resultado)

resultado = soma(5, 5) - 2 * 5
print(resultado)

