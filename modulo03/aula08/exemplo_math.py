import math

valor_ceil = math.ceil(1.001)
print("ceil:", valor_ceil)

valor_floor = math.floor(1.001)
print("floor:", valor_floor)

valor_factorial = math.factorial(10)
print("factorial:", valor_factorial)

valor_gcd = math.gcd(10, 125)
print("gcd:", valor_gcd)

valor_trunc = math.trunc(1.001)
print("trunc:", valor_trunc)

valor_trunc = math.trunc(1.999)
print("trunc:", valor_trunc)
